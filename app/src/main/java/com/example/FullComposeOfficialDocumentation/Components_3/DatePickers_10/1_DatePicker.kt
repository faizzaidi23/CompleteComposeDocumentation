package com.example.FullComposeOfficialDocumentation.Components_3.DatePickers_10

/*
Date pickers let users select a date, a date range, or both.
They use a calendar dialog or text input to let users select dates.

Types:
- Docked: Appears inline within the layout
- Modal: Appears as a dialog overlaying the app's content
- Modal input: Combines a text field with a modal date picker

Composables:
- DatePicker: General composable for a date picker
- DatePickerDialog: Container for modal and modal input date pickers
- DateRangePicker: For selecting a range with start and end date

State:
- DatePickerState: Captures information about the user's selection
- DateRangePickerState: For date range selections
*/