/*
Navigation bar allows users to switch between destinations in an app.

Use navigation bars for:
- Three to five destinations of equal importance
- Compact window sizes
- Consistent destinations across app screens

API Surface:
NavigationBar and NavigationBarItem composables implement destination switching logic.

NavigationBarItem key parameters:
- selected: Determines whether the current item is visually highlighted
- onClick(): Defines the action when user clicks the item
- label: Displays text within the item (optional)
- icon: Displays an icon within the item (optional)
*/